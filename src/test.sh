#!/bin/bash

echo "====================================="
echo "جاري بدء عملية الفحص (Testing)..."
echo "====================================="

# تشغيل ملف الجافا سكريبت باستخدام Node.js
node app.js

# فحص حالة الخروج (Exit Status) للأمر السابق
# إذا كانت القيمة 0 تعني أن الملف اشتغل بدون أي أخطاء برمجة
if [ $? -eq 0 ]; then
    echo "-------------------------------------"
    echo "✅ نجح الاختبار: ملف app.js يعمل بشكل ممتاز!"
    echo "====================================="
else
    echo "-------------------------------------"
    echo "❌ فشل الاختبار: هناك مشكلة في ملف app.js."
    echo "====================================="
fi